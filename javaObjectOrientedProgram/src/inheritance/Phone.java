package inheritance;
// Phone 을 가지고 상속의미 학습
public class Phone {
	private String company;
	private String call; // 디폴트 생성자 생략
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getCall() {
		return call;
	}
	public void setCall(String call) {
		this.call = call;
	}
	
	
}
